first_number = int(input('Digite o primeiro numero: '))
second_number = int(input('Digite o segundo numero: '))
number = float(input('Digite um número real: '))

print('O produto do dobro do primeiro com a metade do segundo é', round((first_number * 2) / second_number))
print('A soma do triplo do primeiro com o terceiro é', round(first_number ** 3  + number))
print('O terceiro elevado ao cubo é', number ** number)